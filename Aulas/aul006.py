#n1 e onde e armazenado valor 1 digitado 
n1 = int(input('Digite um numero: '))
#n2 e onde armazena valor 2 
n2 = int(input('Digite outro numero: '))
#s onde é armazenado valor da soma entre n1+n2
s = n1+n2
##comando print simples para imprimir na tela 
print('A soma Vale',s)
##comando print avançado com mais recurso
print('A soma vale {}'.format(s))

