#Operadores Aritiméticos e Ordem de Precedencia
n1 = int(input('Digite um valor: '))
n2 = int(input('Digite um valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A soma é {} \n o produto é {} \n e a divisao é {}'.format(s, m, d))
print('Divisão inteira {} \n e a potencia {}'.format(di, e))
